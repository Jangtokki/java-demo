package Project;

public class Sub {
	
	int number;
	String title;
	int price;
	int amount;
	
	public Sub() {
		
	}

	public Sub(int number, String title, int price, int amount) {
		
		this.number = number;
		this.title = title;
		this.price = price;
		this.amount = amount;
	}

	public int getNumber() {
		return number;
	}

	public void setNumber(int number) {
		this.number = number;
	}

	public String getTitle() {
		return title;
	}

	public void setTitle(String title) {
		this.title = title;
	}

	public int getPrice() {
		return price;
	}

	public void setPrice(int price) {
		this.price = price;
	}

	public int getAmount() {
		return amount;
	}

	public void setAmount(int amount) {
		this.amount = amount;
	}
	
	

}
