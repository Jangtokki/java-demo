package basic;

public class Java_015_operators {
	public static void main(String[] args) {
		
		int x = 3;
		int res = 0 ;
		res = 3 +2 ;
		
		System.out.println(res);
		
		//x = x+1
		x += 1;
		System.out.printf("x=%d\n", x);
		
		x -= 1;
		System.out.printf("x=%d\n", x);
		
		x*=1;
		System.out.printf("x=%d\n", x);
		
		
	}
}
