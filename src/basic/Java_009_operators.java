package basic;

public class Java_009_operators {

	public static void main(String[] args) {
		int x = 3;
		
		
		//범위 연산자
		++x; // x+=1

		System.out.println(x);
		
		--x;
		System.out.println(x);
		
		
		int y = 5;
		
		//후위 연산자
		y++;
		
		System.out.println(y);
		
		y--;
		System.out.println(y);
		
		//리터널은 증감연산자를 사용할 수없다.
		//10 ++;
		
		//상수 : 한번만 기억할 수 있는 메모리 공간
		final int NUM = 4;
		//상수는 증감연산자를 사용 할 수 없다
		//NUM++;
		
		
		
		
		
		
	}

}
